package slide1Ex03;

public class Alarm {
	private String dipertar;
	private String horas;
	
	public String getDipertar() {
		return dipertar;
	}
	public void setDipertar(String dipertar) {
		this.dipertar = dipertar;
	}
	public String getHoras() {
		return horas;
	}
	public void setHoras(String horas) {
		this.horas = horas;
	}
	
	
}
